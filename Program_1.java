package basics;
// calculate Quotient and Remainder from the given dividend and divisor?..
public class Program_1 {
	public static void main(String args[])
			{
		      int dividend=26,divisor=3;
		      int quotient, remainder;
		      System.out.println("Dividend is:"+dividend);
		      System.out.println("Divisor is:"+divisor);
		      quotient=(dividend/divisor);
		      remainder=(dividend%divisor);
		      System.out.println("Quotient is:"+quotient);   //8
		      System.out.println("Remainder is:"+remainder); // 2
		      
		      
			}

}
